const dangerRoadClearanceMeters = 14;
const dangerWarningMarkerMinZoom = 15;
const fixedDangerWarningMarkers = [];

// Линии и крайние предупреждающие маркеры переключаются как один слой.
function syncDangerRoadVisibility() {
  const shouldBeVisible = map.getZoom() >= dangerWarningMarkerMinZoom;
  const dangerLayers = [
    ...fixedDangerRoads.map((road) => road.lineLayer).filter(Boolean),
    ...fixedDangerWarningMarkers,
  ];

  dangerLayers.forEach((layer) => {
    const isVisible = map.hasLayer(layer);

    if (shouldBeVisible && !isVisible) {
      layer.addTo(map);
    } else if (!shouldBeVisible && isVisible) {
      map.removeLayer(layer);
    }
  });
}

// Интерполирует точку по доле полной длины полилинии.
function getDangerRoadPoint(path, fraction) {
  const lengths = [];
  let totalLength = 0;

  for (let index = 1; index < path.length; index += 1) {
    const length = map.distance(path[index - 1], path[index]);
    lengths.push(length);
    totalLength += length;
  }

  const targetDistance = totalLength * fraction;
  let passedDistance = 0;

  for (let index = 0; index < lengths.length; index += 1) {
    const nextDistance = passedDistance + lengths[index];

    if (nextDistance >= targetDistance) {
      const segmentFraction =
        lengths[index] > 0
          ? (targetDistance - passedDistance) / lengths[index]
          : 0;
      const start = path[index];
      const end = path[index + 1];

      return [
        start[0] + (end[0] - start[0]) * segmentFraction,
        start[1] + (end[1] - start[1]) * segmentFraction,
      ];
    }

    passedDistance = nextDistance;
  }

  return path[path.length - 1];
}

