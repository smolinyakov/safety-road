// Создаёт постоянный слой опасных участков.
function drawFixedDangerRoads() {
  const paneName = "fixed-danger-roads";
  const pane = map.getPane(paneName) ?? map.createPane(paneName);
  pane.style.zIndex = "430";

  fixedDangerRoads.forEach((dangerRoad) => {
    const popupContent = `
      <div class="danger-road-popup">
        <strong>Опасный участок дороги</strong>
        <span>${dangerRoad.school}</span>
        <small>${dangerRoad.street}. Маршруты строятся в обход.</small>
      </div>
    `;
    const line = L.polyline(dangerRoad.path, {
      pane: paneName,
      className: "danger-road-segment",
      color: "#d93025",
      weight: 8,
      opacity: 1,
      lineCap: "round",
      lineJoin: "round",
    }).addTo(map);

    line.bindPopup(popupContent);

    [0, 1].forEach((fraction) => {
      const warningMarker = L.marker(
        getDangerRoadPoint(dangerRoad.path, fraction),
        {
          icon: createDangerRoadWarningIcon(),
          zIndexOffset: 1200,
        },
      );

      warningMarker.bindPopup(popupContent);
      fixedDangerWarningMarkers.push(warningMarker);
    });

    dangerRoad.lineLayer = line;
  });
}

// Проецирует координаты в локальные метры для геометрических тестов.
function projectToLocalMeters(point, referenceLatitude) {
  const metersPerDegree = 111320;
  const longitudeScale = Math.cos((referenceLatitude * Math.PI) / 180);

  return {
    x: point[0] * metersPerDegree * longitudeScale,
    y: point[1] * metersPerDegree,
  };
}

function pointToSegmentDistanceMeters(point, start, end) {
  const referenceLatitude = (point[1] + start[1] + end[1]) / 3;
  const projectedPoint = projectToLocalMeters(point, referenceLatitude);
  const projectedStart = projectToLocalMeters(start, referenceLatitude);
  const projectedEnd = projectToLocalMeters(end, referenceLatitude);
  const segmentX = projectedEnd.x - projectedStart.x;
  const segmentY = projectedEnd.y - projectedStart.y;
  const segmentLengthSquared = segmentX ** 2 + segmentY ** 2;

  if (!segmentLengthSquared) {
    return Math.hypot(
      projectedPoint.x - projectedStart.x,
      projectedPoint.y - projectedStart.y,
    );
  }

  const fraction = Math.max(
    0,
    Math.min(
      1,
      ((projectedPoint.x - projectedStart.x) * segmentX +
        (projectedPoint.y - projectedStart.y) * segmentY) /
        segmentLengthSquared,
    ),
  );
  const nearestX = projectedStart.x + segmentX * fraction;
  const nearestY = projectedStart.y + segmentY * fraction;

  return Math.hypot(projectedPoint.x - nearestX, projectedPoint.y - nearestY);
}

function segmentsIntersect(firstStart, firstEnd, secondStart, secondEnd) {
  const cross = (a, b, c) =>
    (b.x - a.x) * (c.y - a.y) - (b.y - a.y) * (c.x - a.x);
  const referenceLatitude =
    (firstStart[1] + firstEnd[1] + secondStart[1] + secondEnd[1]) / 4;
  const a = projectToLocalMeters(firstStart, referenceLatitude);
  const b = projectToLocalMeters(firstEnd, referenceLatitude);
  const c = projectToLocalMeters(secondStart, referenceLatitude);
  const d = projectToLocalMeters(secondEnd, referenceLatitude);
  const boundsOverlap =
    Math.max(Math.min(a.x, b.x), Math.min(c.x, d.x)) <=
      Math.min(Math.max(a.x, b.x), Math.max(c.x, d.x)) + 0.01 &&
    Math.max(Math.min(a.y, b.y), Math.min(c.y, d.y)) <=
      Math.min(Math.max(a.y, b.y), Math.max(c.y, d.y)) + 0.01;

  if (!boundsOverlap) return false;

  return (
    cross(a, b, c) * cross(a, b, d) <= 0 &&
    cross(c, d, a) * cross(c, d, b) <= 0
  );
}

function segmentDistanceMeters(firstStart, firstEnd, secondStart, secondEnd) {
  if (segmentsIntersect(firstStart, firstEnd, secondStart, secondEnd)) return 0;

  return Math.min(
    pointToSegmentDistanceMeters(firstStart, secondStart, secondEnd),
    pointToSegmentDistanceMeters(firstEnd, secondStart, secondEnd),
    pointToSegmentDistanceMeters(secondStart, firstStart, firstEnd),
    pointToSegmentDistanceMeters(secondEnd, firstStart, firstEnd),
  );
}

// Возвращает участки, пересекающиеся с геометрией маршрута.
function getCrossedDangerRoads(route, clearance = dangerRoadClearanceMeters) {
  const routePoints = route?.geometry?.coordinates ?? [];
  if (routePoints.length < 2) return [];

  return fixedDangerRoads.filter((dangerRoad) => {
    const dangerPoints = dangerRoad.path.map(([latitude, longitude]) => [
      longitude,
      latitude,
    ]);

    for (let routeIndex = 1; routeIndex < routePoints.length; routeIndex += 1) {
      for (let dangerIndex = 1; dangerIndex < dangerPoints.length; dangerIndex += 1) {
        if (
          segmentDistanceMeters(
            routePoints[routeIndex - 1],
            routePoints[routeIndex],
            dangerPoints[dangerIndex - 1],
            dangerPoints[dangerIndex],
          ) <= clearance
        ) {
          return true;
        }
      }
    }

    return false;
  });
}

function routeAvoidsFixedDangerRoads(route) {
  return getCrossedDangerRoads(route).length === 0;
}

function filterRoutesAvoidingFixedDangerRoads(routes) {
  return routes.filter(routeAvoidsFixedDangerRoads);
}

// Рассчитывает промежуточные точки для запросов обходного маршрута.
function makeDangerRoadDetourPoints(routes, offsetMeters = 95) {
  const crossedRoads = new Map();

  routes.forEach((route) => {
    getCrossedDangerRoads(route).forEach((dangerRoad) => {
      crossedRoads.set(dangerRoad.id, dangerRoad);
    });
  });

  return [...crossedRoads.values()].slice(0, 2).flatMap((dangerRoad) => {
    const start = dangerRoad.path[0];
    const end = dangerRoad.path[dangerRoad.path.length - 1];
    const latitude = (start[0] + end[0]) / 2;
    const longitude = (start[1] + end[1]) / 2;
    const latitudeMeters = (end[0] - start[0]) * 111320;
    const longitudeMeters =
      (end[1] - start[1]) * 111320 * Math.cos((latitude * Math.PI) / 180);
    const length = Math.hypot(longitudeMeters, latitudeMeters) || 1;
    const offsetX = (-latitudeMeters / length) * offsetMeters;
    const offsetY = (longitudeMeters / length) * offsetMeters;
    const longitudeScale = 111320 * Math.cos((latitude * Math.PI) / 180);

    return [1, -1].map((side) => [
      latitude + (offsetY * side) / 111320,
      longitude + (offsetX * side) / longitudeScale,
    ]);
  });
}

